// Eligibility helper: max loan based on salary and remaining years until age 60
export function maxEligibleLoan(salary: number, age: number, ratePct = 6.5): number {
  const yearsLeft = Math.max(1, Math.min(25, 60 - age));
  const months = yearsLeft * 12;
  const maxMonthly = salary * 0.4;
  const r = ratePct / 100 / 12;
  // present value of annuity formula
  if (r === 0) return maxMonthly * months;
  return (maxMonthly * (1 - Math.pow(1 + r, -months))) / r;
}
